thread_local! {
    /// WinRT delegates generated by `windows-bindgen` require `Send`, while elwindui's UI state is
    /// intentionally single-threaded (`Rc<RefCell<_>>`). WinUI invokes these delegates on this UI
    /// thread, so the delegate captures only a numeric key and resolves the actual callback here.
    static UI_EVENT_CALLBACKS: RefCell<HashMap<usize, Rc<dyn Fn()>>> = RefCell::new(HashMap::new());
    static UI_INDEX_EVENT_CALLBACKS: RefCell<HashMap<usize, Rc<dyn Fn(usize)>>> = RefCell::new(HashMap::new());
    static UI_KEY_EVENT_CALLBACKS: RefCell<HashMap<usize, Rc<dyn Fn(RawKeyEvent)>>> = RefCell::new(HashMap::new());
    static UI_TEXT_EVENT_CALLBACKS: RefCell<HashMap<usize, Rc<dyn Fn(String)>>> = RefCell::new(HashMap::new());
}

static NEXT_UI_EVENT_CALLBACK: AtomicUsize = AtomicUsize::new(1);

fn register_ui_event_callback(callback: Rc<dyn Fn()>) -> usize {
    let id = NEXT_UI_EVENT_CALLBACK.fetch_add(1, Ordering::Relaxed);
    UI_EVENT_CALLBACKS.with(|callbacks| {
        callbacks.borrow_mut().insert(id, callback);
    });
    id
}

fn invoke_ui_event_callback(id: usize) {
    // Clone the `Rc` out and drop the borrow *before* calling it — the callback body (e.g. opening
    // a file, which can synchronously rebuild native controls and register new callbacks) can
    // legitimately re-enter `register_ui_event_callback`/this same map. Holding `borrow()` across
    // the call would make that a `RefCell` double-borrow panic (`already borrowed`), not just a
    // theoretical risk — reproduced via `notepad`'s File > Open.
    let callback = UI_EVENT_CALLBACKS.with(|callbacks| callbacks.borrow().get(&id).cloned());
    if let Some(callback) = callback {
        callback();
    }
}

fn register_ui_index_event_callback(callback: Rc<dyn Fn(usize)>) -> usize {
    let id = NEXT_UI_EVENT_CALLBACK.fetch_add(1, Ordering::Relaxed);
    UI_INDEX_EVENT_CALLBACKS.with(|callbacks| { callbacks.borrow_mut().insert(id, callback); });
    id
}

fn invoke_ui_index_event_callback(id: usize, index: usize) {
    // See `invoke_ui_event_callback`'s doc comment — same re-entrancy hazard, same fix.
    let callback = UI_INDEX_EVENT_CALLBACKS.with(|callbacks| callbacks.borrow().get(&id).cloned());
    if let Some(callback) = callback {
        callback(index);
    }
}

