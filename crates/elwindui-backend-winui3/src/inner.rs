fn win2d_path_geometry(
    creator: &ICanvasResourceCreator,
    commands: &[elwindui_core::graphics::PathCommand],
    origin_x: f32,
    origin_y: f32,
    rule: elwindui_core::graphics::FillRule,
) -> Result<CanvasGeometry> {
    use elwindui_core::graphics::{PathCommand, SweepDirection};

    let builder = CanvasPathBuilder::Create(creator)?;
    let determination = match rule {
        elwindui_core::graphics::FillRule::EvenOdd => CanvasFilledRegionDetermination::Alternate,
        elwindui_core::graphics::FillRule::NonZero => CanvasFilledRegionDetermination::Winding,
    };
    builder.SetFilledRegionDetermination(determination)?;
    let mut open = false;
    for command in commands {
        match command {
            PathCommand::MoveTo(point) => {
                if open { builder.EndFigure(CanvasFigureLoop::Open)?; }
                builder.BeginFigureAtCoords(point.x + origin_x, point.y + origin_y)?;
                open = true;
            }
            PathCommand::LineTo(point) if open => {
                builder.AddLineWithCoords(point.x + origin_x, point.y + origin_y)?;
            }
            PathCommand::QuadTo { control, to } if open => {
                builder.AddQuadraticBezier(
                    windows_numerics::Vector2 { X: control.x + origin_x, Y: control.y + origin_y },
                    windows_numerics::Vector2 { X: to.x + origin_x, Y: to.y + origin_y },
                )?;
            }
            PathCommand::CubicTo { control1, control2, to } if open => {
                builder.AddCubicBezier(
                    windows_numerics::Vector2 { X: control1.x + origin_x, Y: control1.y + origin_y },
                    windows_numerics::Vector2 { X: control2.x + origin_x, Y: control2.y + origin_y },
                    windows_numerics::Vector2 { X: to.x + origin_x, Y: to.y + origin_y },
                )?;
            }
            PathCommand::ArcTo(arc) if open => {
                builder.AddArcToPoint(
                    windows_numerics::Vector2 { X: arc.to.x + origin_x, Y: arc.to.y + origin_y },
                    arc.radii.width,
                    arc.radii.height,
                    arc.x_axis_rotation,
                    match arc.sweep {
                        SweepDirection::Clockwise => CanvasSweepDirection::Clockwise,
                        SweepDirection::CounterClockwise => CanvasSweepDirection::CounterClockwise,
                    },
                    if arc.large_arc { CanvasArcSize::Large } else { CanvasArcSize::Small },
                )?;
            }
            PathCommand::Close if open => {
                builder.EndFigure(CanvasFigureLoop::Closed)?;
                open = false;
            }
            _ => {}
        }
    }
    if open { builder.EndFigure(CanvasFigureLoop::Open)?; }
    CanvasGeometry::CreatePath(&builder)
}

fn win2d_clip_geometry(
    creator: &ICanvasResourceCreator,
    clip: &elwindui_core::graphics::Clip,
    origin_x: f32,
    origin_y: f32,
) -> Result<CanvasGeometry> {
    match clip {
        elwindui_core::graphics::Clip::Rect(rect) => CanvasGeometry::CreateRectangleAtCoords(
            creator, rect.x + origin_x, rect.y + origin_y, rect.width, rect.height,
        ),
        elwindui_core::graphics::Clip::RoundedRect { rect, radii } => {
            let radius = (radii.top_left + radii.top_right + radii.bottom_right + radii.bottom_left) / 4.0;
            CanvasGeometry::CreateRoundedRectangleAtCoords(
                creator, rect.x + origin_x, rect.y + origin_y, rect.width, rect.height, radius, radius,
            )
        }
        elwindui_core::graphics::Clip::Path { path, rule } => {
            win2d_path_geometry(creator, path.commands(), origin_x, origin_y, *rule)
        }
    }
}

fn win2d_brush_matrix(transform: elwindui_core::base::AffineTransform) -> windows_numerics::Matrix3x2 {
    windows_numerics::Matrix3x2 {
        M11: transform.m11,
        M12: transform.m12,
        M21: transform.m21,
        M22: transform.m22,
        M31: transform.dx,
        M32: transform.dy,
    }
}

fn win2d_gradient_point(
    point: elwindui_core::base::Point,
    mapping: elwindui_core::graphics::BrushMappingMode,
    bounds: elwindui_core::base::Rect,
) -> windows_numerics::Vector2 {
    use elwindui_core::graphics::BrushMappingMode;
    match mapping {
        BrushMappingMode::RelativeToBounds => windows_numerics::Vector2 {
            X: bounds.x + point.x * bounds.width,
            Y: bounds.y + point.y * bounds.height,
        },
        BrushMappingMode::Absolute => windows_numerics::Vector2 { X: point.x, Y: point.y },
    }
}

fn win2d_gradient_radius(
    radius: f32,
    mapping: elwindui_core::graphics::BrushMappingMode,
    extent: f32,
) -> f32 {
    match mapping {
        elwindui_core::graphics::BrushMappingMode::RelativeToBounds => radius * extent,
        elwindui_core::graphics::BrushMappingMode::Absolute => radius,
    }
}

fn win2d_gradient_stops(stops: &[elwindui_core::graphics::GradientStop]) -> Vec<CanvasGradientStop> {
    stops
        .iter()
        .map(|stop| CanvasGradientStop {
            Position: stop.offset,
            Color: graphics_color_to_winui_color(stop.color),
        })
        .collect()
}

fn win2d_path_bounds(commands: &[elwindui_core::graphics::PathCommand]) -> elwindui_core::base::Rect {
    use elwindui_core::graphics::{PathBuilder, PathCommand};

    let mut builder = PathBuilder::new();
    for command in commands {
        match command {
            PathCommand::MoveTo(point) => { builder.move_to(*point); }
            PathCommand::LineTo(point) => { builder.line_to(*point); }
            PathCommand::QuadTo { control, to } => { builder.quad_to(*control, *to); }
            PathCommand::CubicTo { control1, control2, to } => { builder.cubic_to(*control1, *control2, *to); }
            PathCommand::ArcTo(arc) => { builder.arc_to(*arc); }
            PathCommand::Close => { builder.close(); }
        }
    }
    builder
        .build()
        .map(|path| path.bounds())
        .unwrap_or(elwindui_core::base::Rect { x: 0.0, y: 0.0, width: 0.0, height: 0.0 })
}

/// Materializes one core brush inside the current CanvasControl draw callback. Win2D resources
/// are deliberately not retained in `Win2dPrimitive`: that keeps the retained scene device-loss
/// safe and ensures every resource belongs to the drawing session's own device.
fn win2d_brush(
    creator: &ICanvasResourceCreator,
    brush: &elwindui_core::graphics::Brush,
    bounds: elwindui_core::base::Rect,
    opacity: f32,
) -> Result<ICanvasBrush> {
    use elwindui_core::graphics::Brush;

    match brush {
        Brush::Solid(color) => {
            let native = CanvasSolidColorBrush::Create(creator, graphics_color_to_winui_color(*color))?;
            native.SetOpacity(opacity.clamp(0.0, 1.0))?;
            native.cast()
        }
        Brush::LinearGradient(gradient) => {
            let stops = win2d_gradient_stops(&gradient.stops);
            let native = CanvasLinearGradientBrush::CreateWithStops(creator, &stops)?;
            native.SetStartPoint(win2d_gradient_point(gradient.start, gradient.mapping, bounds))?;
            native.SetEndPoint(win2d_gradient_point(gradient.end, gradient.mapping, bounds))?;
            native.SetTransform(win2d_brush_matrix(gradient.transform))?;
            native.SetOpacity((gradient.opacity * opacity).clamp(0.0, 1.0))?;
            native.cast()
        }
        Brush::RadialGradient(gradient) => {
            let stops = win2d_gradient_stops(&gradient.stops);
            let native = CanvasRadialGradientBrush::CreateWithStops(creator, &stops)?;
            let center = win2d_gradient_point(gradient.center, gradient.mapping, bounds);
            let origin = win2d_gradient_point(gradient.gradient_origin, gradient.mapping, bounds);
            native.SetCenter(center)?;
            native.SetOriginOffset(windows_numerics::Vector2 {
                X: origin.X - center.X,
                Y: origin.Y - center.Y,
            })?;
            native.SetRadiusX(win2d_gradient_radius(gradient.radius_x, gradient.mapping, bounds.width))?;
            native.SetRadiusY(win2d_gradient_radius(gradient.radius_y, gradient.mapping, bounds.height))?;
            native.SetTransform(win2d_brush_matrix(gradient.transform))?;
            native.SetOpacity((gradient.opacity * opacity).clamp(0.0, 1.0))?;
            native.cast()
        }
        Brush::Image(image_brush) => {
            let bitmap = win2d_bitmap(creator, &image_brush.image)?;
            let native = CanvasImageBrush::CreateWithImage(creator, &bitmap)?;
            let bitmap_size = bitmap.SizeInPixels()?;
            let source = image_brush.source_rect.unwrap_or(elwindui_core::base::Rect {
                x: 0.0,
                y: 0.0,
                width: bitmap_size.Width as f32,
                height: bitmap_size.Height as f32,
            });
            if image_brush.source_rect.is_some() {
                let source = windows::Foundation::Rect {
                    X: source.x,
                    Y: source.y,
                    Width: source.width,
                    Height: source.height,
                };
                let reference: windows::Foundation::IReference<windows::Foundation::Rect> =
                    PropertyValue::CreateRect(source)?.cast()?;
                native.SetSourceRectangle(&reference)?;
            }
            let (extend_x, extend_y) = match image_brush.tile_mode {
                elwindui_core::graphics::TileMode::None => (CanvasEdgeBehavior::Clamp, CanvasEdgeBehavior::Clamp),
                elwindui_core::graphics::TileMode::Tile => (CanvasEdgeBehavior::Wrap, CanvasEdgeBehavior::Wrap),
                elwindui_core::graphics::TileMode::FlipX => (CanvasEdgeBehavior::Mirror, CanvasEdgeBehavior::Clamp),
                elwindui_core::graphics::TileMode::FlipY => (CanvasEdgeBehavior::Clamp, CanvasEdgeBehavior::Mirror),
                elwindui_core::graphics::TileMode::FlipXY => (CanvasEdgeBehavior::Mirror, CanvasEdgeBehavior::Mirror),
            };
            native.SetExtendX(extend_x)?;
            native.SetExtendY(extend_y)?;
            native.SetInterpolation(CanvasImageInterpolation::Linear)?;
            let fit = match image_brush.stretch {
                elwindui_core::graphics::Stretch::None => elwindui_core::graphics::ImageFit::None,
                elwindui_core::graphics::Stretch::Fill => elwindui_core::graphics::ImageFit::Fill,
                elwindui_core::graphics::Stretch::Uniform => elwindui_core::graphics::ImageFit::Contain,
                elwindui_core::graphics::Stretch::UniformToFill => elwindui_core::graphics::ImageFit::Cover,
            };
            let layout = win2d_fitted_image_rect(
                bounds,
                (source.width, source.height),
                &elwindui_core::graphics::ImageDrawOptions {
                    opacity: 1.0,
                    sampling: elwindui_core::graphics::ImageSampling::Linear,
                    fit,
                    alignment_x: image_brush.alignment_x,
                    alignment_y: image_brush.alignment_y,
                    repeat: image_brush.tile_mode,
                },
            );
            let layout_transform = elwindui_core::base::AffineTransform::translation(layout.x, layout.y)
                .concat(&elwindui_core::base::AffineTransform::scale(
                    layout.width / source.width.max(1e-6),
                    layout.height / source.height.max(1e-6),
                ))
                .concat(&elwindui_core::base::AffineTransform::translation(-source.x, -source.y));
            native.SetTransform(win2d_brush_matrix(layout_transform.concat(&image_brush.transform)))?;
            native.SetOpacity((image_brush.opacity * opacity).clamp(0.0, 1.0))?;
            native.cast()
        }
    }
}

#[allow(dead_code)]
fn win2d_stroke_style(
    stroke: &elwindui_core::graphics::StrokeStyle,
) -> Result<crate::bindings::Microsoft::Graphics::Canvas::Geometry::CanvasStrokeStyle> {
    let native = crate::bindings::Microsoft::Graphics::Canvas::Geometry::CanvasStrokeStyle::new()?;
    native.SetMiterLimit(stroke.miter_limit)?;
    let cap = |cap| match cap {
        elwindui_core::graphics::LineCap::Butt => crate::bindings::Microsoft::Graphics::Canvas::Geometry::CanvasCapStyle::Flat,
        elwindui_core::graphics::LineCap::Round => crate::bindings::Microsoft::Graphics::Canvas::Geometry::CanvasCapStyle::Round,
        elwindui_core::graphics::LineCap::Square => crate::bindings::Microsoft::Graphics::Canvas::Geometry::CanvasCapStyle::Square,
    };
    native.SetStartCap(cap(stroke.start_cap))?;
    native.SetEndCap(cap(stroke.end_cap))?;
    native.SetDashCap(cap(stroke.dash_cap))?;
    native.SetDashOffset(stroke.dash_offset)?;
    native.SetCustomDashStyle(&stroke.dash_pattern)?;
    native.SetLineJoin(match stroke.line_join {
        elwindui_core::graphics::LineJoin::Miter => crate::bindings::Microsoft::Graphics::Canvas::Geometry::CanvasLineJoin::Miter,
        elwindui_core::graphics::LineJoin::Round => crate::bindings::Microsoft::Graphics::Canvas::Geometry::CanvasLineJoin::Round,
        elwindui_core::graphics::LineJoin::Bevel => crate::bindings::Microsoft::Graphics::Canvas::Geometry::CanvasLineJoin::Bevel,
    })?;
    Ok(native)
}

fn win2d_bitmap(
    creator: &ICanvasResourceCreator,
    image: &elwindui_core::graphics::Image,
) -> Result<CanvasBitmap> {
    use elwindui_core::graphics::{AlphaMode, ImageData};

    match image.data() {
        ImageData::Encoded { bytes, .. } => {
            let stream = InMemoryRandomAccessStream::new()?;
            let writer = DataWriter::CreateDataWriter(&stream)?;
            writer.WriteBytes(bytes)?;
            writer.StoreAsync()?.join()?;
            let stream: IRandomAccessStream = stream.cast()?;
            stream.Seek(0)?;
            CanvasBitmap::LoadAsyncFromStream(creator, &stream)?.join()
        }
        ImageData::Rgba8 { width, height, stride, pixels, alpha } => {
            let mut bgra = Vec::with_capacity((*width as usize) * (*height as usize) * 4);
            for row in 0..*height as usize {
                let row_start = row * *stride as usize;
                for pixel in pixels[row_start..row_start + *width as usize * 4].chunks_exact(4) {
                    let (r, g, b, a) = (pixel[0], pixel[1], pixel[2], pixel[3]);
                    let (r, g, b) = match alpha {
                        AlphaMode::Straight => (
                            ((r as u16 * a as u16 + 127) / 255) as u8,
                            ((g as u16 * a as u16 + 127) / 255) as u8,
                            ((b as u16 * a as u16 + 127) / 255) as u8,
                        ),
                        AlphaMode::Premultiplied => (r, g, b),
                        AlphaMode::Opaque => (r, g, b),
                    };
                    bgra.extend_from_slice(&[b, g, r, if *alpha == AlphaMode::Opaque { 255 } else { a }]);
                }
            }
            CanvasBitmap::CreateFromBytes(
                creator,
                &bgra,
                (*width).try_into().unwrap(),
                (*height).try_into().unwrap(),
                DirectXPixelFormat::B8G8R8A8UIntNormalized,
            )
        }
        ImageData::Backend(handle) => {
            handle.0.downcast_ref::<CanvasBitmap>().cloned().ok_or_else(|| {
                windows::core::Error::new(
                    windows::core::HRESULT(0x80070057_u32 as i32),
                    "Image backend handle is not a Win2D CanvasBitmap",
                )
            })
        }
    }
}

fn win2d_fitted_image_rect(
    dest: elwindui_core::base::Rect,
    image_size: (f32, f32),
    options: &elwindui_core::graphics::ImageDrawOptions,
) -> elwindui_core::base::Rect {
    use elwindui_core::graphics::{AlignmentX, AlignmentY, ImageFit};
    let (iw, ih) = image_size;
    let (width, height) = if iw <= 0.0 || ih <= 0.0 {
        (dest.width, dest.height)
    } else {
        match options.fit {
            ImageFit::Fill => (dest.width, dest.height),
            ImageFit::None => (iw, ih),
            ImageFit::Contain => {
                let scale = (dest.width / iw).min(dest.height / ih);
                (iw * scale, ih * scale)
            }
            ImageFit::Cover => {
                let scale = (dest.width / iw).max(dest.height / ih);
                (iw * scale, ih * scale)
            }
        }
    };
    let x = match options.alignment_x {
        AlignmentX::Left => dest.x,
        AlignmentX::Center => dest.x + (dest.width - width) / 2.0,
        AlignmentX::Right => dest.x + dest.width - width,
    };
    let y = match options.alignment_y {
        AlignmentY::Top => dest.y,
        AlignmentY::Center => dest.y + (dest.height - height) / 2.0,
        AlignmentY::Bottom => dest.y + dest.height - height,
    };
    elwindui_core::base::Rect { x, y, width, height }
}

fn push_win2d_transform(
    out: &mut Vec<Win2dPrimitive>,
    transform: elwindui_core::base::AffineTransform,
) {
    out.push(Win2dPrimitive::SetTransform {
        m11: transform.m11,
        m12: transform.m12,
        m21: transform.m21,
        m22: transform.m22,
        dx: transform.dx,
        dy: transform.dy,
    });
}

fn win2d_fitted_vector_rect(
    dest: elwindui_core::base::Rect,
    source_size: (f32, f32),
    options: &elwindui_core::graphics::VectorImageDrawOptions,
) -> elwindui_core::base::Rect {
    let image_options = elwindui_core::graphics::ImageDrawOptions {
        opacity: 1.0,
        sampling: elwindui_core::graphics::ImageSampling::Linear,
        fit: options.fit,
        alignment_x: options.alignment_x,
        alignment_y: options.alignment_y,
        repeat: elwindui_core::graphics::TileMode::None,
    };
    win2d_fitted_image_rect(dest, source_size, &image_options)
}

fn svg_view_box_transform(
    source: elwindui_core::base::Rect,
    destination: elwindui_core::base::Rect,
    aspect: elwindui_core::graphics::PreserveAspectRatio,
) -> elwindui_core::base::AffineTransform {
    use elwindui_core::graphics::{
        PreserveAspectRatioAlign as Align, PreserveAspectRatioMeetOrSlice as MeetOrSlice,
    };

    let raw_x = destination.width / source.width.max(1e-6);
    let raw_y = destination.height / source.height.max(1e-6);
    let (scale_x, scale_y, extra_x, extra_y) = if aspect.align == Align::None {
        (raw_x, raw_y, 0.0, 0.0)
    } else {
        let scale = match aspect.meet_or_slice {
            MeetOrSlice::Meet => raw_x.min(raw_y),
            MeetOrSlice::Slice => raw_x.max(raw_y),
        };
        let width = source.width * scale;
        let height = source.height * scale;
        let offset_x = match aspect.align {
            Align::XMinYMin | Align::XMinYMid | Align::XMinYMax => 0.0,
            Align::XMidYMin | Align::XMidYMid | Align::XMidYMax => (destination.width - width) / 2.0,
            Align::XMaxYMin | Align::XMaxYMid | Align::XMaxYMax => destination.width - width,
            Align::None => 0.0,
        };
        let offset_y = match aspect.align {
            Align::XMinYMin | Align::XMidYMin | Align::XMaxYMin => 0.0,
            Align::XMinYMid | Align::XMidYMid | Align::XMaxYMid => (destination.height - height) / 2.0,
            Align::XMinYMax | Align::XMidYMax | Align::XMaxYMax => destination.height - height,
            Align::None => 0.0,
        };
        (scale, scale, offset_x, offset_y)
    };
    elwindui_core::base::AffineTransform::translation(destination.x + extra_x, destination.y + extra_y)
        .concat(&elwindui_core::base::AffineTransform::scale(scale_x, scale_y))
        .concat(&elwindui_core::base::AffineTransform::translation(-source.x, -source.y))
}

fn vector_mask_path(mask: &elwindui_core::graphics::VectorMask) -> elwindui_core::graphics::Path {
    use elwindui_core::graphics::{PathBuilder, VectorNode};

    fn append_group(
        builder: &mut PathBuilder,
        group: &elwindui_core::graphics::VectorGroup,
        parent: elwindui_core::base::AffineTransform,
    ) {
        let world = parent.concat(&group.transform);
        for node in group.children.iter() {
            match node {
                VectorNode::Path(node) if node.visibility => {
                    builder.add_path(&node.path, Some(world.concat(&node.transform)));
                }
                VectorNode::Group(group) => append_group(builder, group, world),
                VectorNode::Path(_) | VectorNode::RasterImage(_) => {}
            }
        }
    }

    let mut builder = PathBuilder::new();
    append_group(&mut builder, &mask.root, mask.transform);
    let local = builder.build().unwrap_or_else(|_| {
        PathBuilder::new().build().expect("an empty PathBuilder must build")
    });
    match &mask.nested {
        Some(nested) => elwindui_core::graphics::Path::combine(
            &local,
            &vector_mask_path(nested),
            elwindui_core::graphics::GeometryCombineMode::Intersect,
            0.25,
        )
        .unwrap_or(local),
        None => local,
    }
}

fn win2d_vector_blend(mode: elwindui_core::graphics::VectorBlendMode) -> Option<CanvasBlend> {
    match mode {
        elwindui_core::graphics::VectorBlendMode::Normal => Some(CanvasBlend::SourceOver),
        // Win2D's Min/Add composition modes are direct GPU implementations of the two blend
        // operations it exposes without first rasterizing a group into a custom effect graph.
        elwindui_core::graphics::VectorBlendMode::Darken => Some(CanvasBlend::Min),
        elwindui_core::graphics::VectorBlendMode::Lighten => Some(CanvasBlend::Add),
        _ => None,
    }
}

/// Expands a retained SVG pattern into its visible tiles and clips those tiles to one path. This
/// stays in the ordinary retained Win2D command stream: no bitmap cache is needed, so the result
/// remains sharp under CanvasControl DPI/device changes.
fn emit_vector_pattern_fill(
    pattern: &elwindui_core::graphics::VectorPattern,
    path: &elwindui_core::graphics::Path,
    rule: elwindui_core::graphics::FillRule,
    stroke_width: Option<f32>,
    node_world: elwindui_core::base::AffineTransform,
    opacity: f32,
    out: &mut Vec<Win2dPrimitive>,
) {
    use elwindui_core::graphics::Clip;

    let tile = pattern.tile_rect;
    let bounds = path.bounds();
    if tile.width <= 0.0 || tile.height <= 0.0 || bounds.width <= 0.0 || bounds.height <= 0.0 {
        return;
    }
    let source = pattern.view_box.unwrap_or(tile);
    if source.width <= 0.0 || source.height <= 0.0 {
        return;
    }

    push_win2d_transform(out, node_world);
    match stroke_width {
        Some(width_px) => out.push(Win2dPrimitive::PushPathStrokeClip {
            commands: path.commands().to_vec(),
            x: 0.0,
            y: 0.0,
            width_px,
        }),
        None => out.push(Win2dPrimitive::PushClip {
            clip: Clip::Path { path: path.clone(), rule },
            x: 0.0,
            y: 0.0,
        }),
    }
    out.push(Win2dPrimitive::PushOpacityLayer(opacity.clamp(0.0, 1.0)));

    let first_x = ((bounds.x - tile.x) / tile.width).floor() as i32;
    let last_x = ((bounds.x + bounds.width - tile.x) / tile.width).ceil() as i32;
    let first_y = ((bounds.y - tile.y) / tile.height).floor() as i32;
    let last_y = ((bounds.y + bounds.height - tile.y) / tile.height).ceil() as i32;
    const MAX_PATTERN_TILES: usize = 4096;
    let mut count = 0usize;
    'tiles: for row in first_y..last_y {
        for column in first_x..last_x {
            if count == MAX_PATTERN_TILES {
                #[cfg(debug_assertions)]
                eprintln!("elwindui-backend-winui3: SVG pattern tile expansion reached its safety limit");
                break 'tiles;
            }
            count += 1;
            let x = tile.x + column as f32 * tile.width;
            let y = tile.y + row as f32 * tile.height;
            let tile_transform = svg_view_box_transform(
                source,
                elwindui_core::base::Rect { x, y, width: tile.width, height: tile.height },
                pattern.preserve_aspect_ratio,
            )
                .concat(&pattern.transform);
            emit_vector_group(&pattern.root, node_world.concat(&tile_transform), 1.0, out);
        }
    }
    out.push(Win2dPrimitive::PopOpacityLayer);
    out.push(Win2dPrimitive::PopClip);
    push_win2d_transform(out, node_world);
}

fn emit_vector_group(
    group: &elwindui_core::graphics::VectorGroup,
    parent_transform: elwindui_core::base::AffineTransform,
    base_opacity: f32,
    out: &mut Vec<Win2dPrimitive>,
) {
    use elwindui_core::graphics::{
        Clip, ImageDrawOptions, ImageFit, TileMode, VectorNode, VectorPaintOrder,
    };

    let world = parent_transform.concat(&group.transform);
    if win2d_vector_blend(group.blend_mode).is_none() || group.isolate || !group.filters.is_empty() {
        #[cfg(debug_assertions)]
        eprintln!("elwindui-backend-winui3: SVG blend, isolation, mask, or filter requires an offscreen effect graph and is not yet supported");
    }
    if let Some(blend) = win2d_vector_blend(group.blend_mode) {
        out.push(Win2dPrimitive::SetBlend(blend));
    }
    push_win2d_transform(out, world);
    if let Some(clip_path) = &group.clip_path {
        out.push(Win2dPrimitive::PushClip {
            clip: Clip::Path {
                path: clip_path.to_path(),
                rule: elwindui_core::graphics::FillRule::NonZero,
            },
            x: 0.0,
            y: 0.0,
        });
    }
    if let Some(mask) = &group.mask {
        if mask.mask_type == elwindui_core::graphics::VectorMaskType::Luminance {
            #[cfg(debug_assertions)]
            eprintln!("elwindui-backend-winui3: SVG luminance masks require an offscreen alpha conversion; using mask geometry");
        }
        out.push(Win2dPrimitive::PushClip {
            clip: Clip::Rect(mask.bounds),
            x: 0.0,
            y: 0.0,
        });
        out.push(Win2dPrimitive::PushClip {
            clip: Clip::Path {
                path: vector_mask_path(mask),
                rule: elwindui_core::graphics::FillRule::NonZero,
            },
            x: 0.0,
            y: 0.0,
        });
    }
    out.push(Win2dPrimitive::PushOpacityLayer(group.opacity.clamp(0.0, 1.0)));

    for node in group.children.iter() {
        match node {
            VectorNode::Group(child) => emit_vector_group(child, world, base_opacity, out),
            VectorNode::Path(path_node) if path_node.visibility => {
                let node_world = world.concat(&path_node.transform);
                push_win2d_transform(out, node_world);
                out.push(Win2dPrimitive::SetAntialiasing(matches!(
                    path_node.rendering,
                    elwindui_core::graphics::VectorShapeRendering::CrispEdges
                        | elwindui_core::graphics::VectorShapeRendering::OptimizeSpeed,
                )));
                let emit_fill = |out: &mut Vec<Win2dPrimitive>| {
                    if let Some(fill) = &path_node.fill {
                        match &fill.paint {
                            elwindui_core::graphics::VectorPaint::Brush(brush) => {
                                out.push(Win2dPrimitive::SetOpacity((base_opacity * fill.opacity).clamp(0.0, 1.0)));
                                out.push(Win2dPrimitive::FillPath {
                                    commands: path_node.path.commands().to_vec(), x: 0.0, y: 0.0,
                                    brush: brush.clone(), rule: fill.rule,
                                });
                                out.push(Win2dPrimitive::SetOpacity(base_opacity));
                            }
                            elwindui_core::graphics::VectorPaint::Pattern(pattern) => {
                                emit_vector_pattern_fill(
                                    pattern,
                                    &path_node.path,
                                    fill.rule,
                                    None,
                                    node_world,
                                    (base_opacity * fill.opacity).clamp(0.0, 1.0),
                                    out,
                                );
                            }
                        }
                    }
                };
                let emit_stroke = |out: &mut Vec<Win2dPrimitive>| {
                    if let Some(stroke) = &path_node.stroke {
                        match &stroke.paint {
                            elwindui_core::graphics::VectorPaint::Brush(brush) => {
                                out.push(Win2dPrimitive::SetOpacity((base_opacity * stroke.opacity).clamp(0.0, 1.0)));
                                out.push(Win2dPrimitive::StrokePath {
                                    commands: path_node.path.commands().to_vec(), x: 0.0, y: 0.0,
                            brush: brush.clone(), stroke: stroke.style.clone(),
                                });
                                out.push(Win2dPrimitive::SetOpacity(base_opacity));
                            }
                            elwindui_core::graphics::VectorPaint::Pattern(pattern) => {
                                emit_vector_pattern_fill(
                                    pattern,
                                    &path_node.path,
                                    elwindui_core::graphics::FillRule::NonZero,
                                    Some(stroke.style.width),
                                    node_world,
                                    (base_opacity * stroke.opacity).clamp(0.0, 1.0),
                                    out,
                                );
                            }
                        }
                    }
                };
                match path_node.paint_order {
                    VectorPaintOrder::FillStroke => { emit_fill(out); emit_stroke(out); }
                    VectorPaintOrder::StrokeFill => { emit_stroke(out); emit_fill(out); }
                }
                out.push(Win2dPrimitive::SetAntialiasing(false));
                push_win2d_transform(out, world);
            }
            VectorNode::Path(_) => {}
            VectorNode::RasterImage(raster) => {
                push_win2d_transform(out, world.concat(&raster.transform));
                out.push(Win2dPrimitive::DrawImage {
                    image: raster.image.clone(),
                    dest: raster.rect,
                    source: None,
                    options: ImageDrawOptions {
                        opacity: raster.opacity,
                        sampling: raster.sampling,
                        fit: ImageFit::Fill,
                        alignment_x: elwindui_core::graphics::AlignmentX::Center,
                        alignment_y: elwindui_core::graphics::AlignmentY::Center,
                        repeat: TileMode::None,
                    },
                    x: 0.0,
                    y: 0.0,
                });
                push_win2d_transform(out, world);
            }
        }
    }
    out.push(Win2dPrimitive::PopOpacityLayer);
    if group.mask.is_some() {
        out.push(Win2dPrimitive::PopClip);
        out.push(Win2dPrimitive::PopClip);
    }
    if group.clip_path.is_some() {
        out.push(Win2dPrimitive::PopClip);
    }
    push_win2d_transform(out, parent_transform);
    out.push(Win2dPrimitive::SetBlend(CanvasBlend::SourceOver));
}

fn emit_vector_image(
    image: &elwindui_core::graphics::VectorImage,
    dest: elwindui_core::base::Rect,
    source: Option<elwindui_core::base::Rect>,
    options: &elwindui_core::graphics::VectorImageDrawOptions,
    parent_transform: elwindui_core::base::AffineTransform,
    base_opacity: f32,
    out: &mut Vec<Win2dPrimitive>,
) {
    let source = source.unwrap_or_else(|| image.view_box());
    if source.width <= 0.0 || source.height <= 0.0 {
        return;
    }
    let placed = win2d_fitted_vector_rect(dest, (source.width, source.height), options);
    let local = elwindui_core::base::AffineTransform::translation(placed.x, placed.y)
        .concat(&elwindui_core::base::AffineTransform::scale(
            placed.width / source.width,
            placed.height / source.height,
        ))
        .concat(&elwindui_core::base::AffineTransform::translation(-source.x, -source.y));
    if options.clip_to_dest {
        push_win2d_transform(out, parent_transform);
        out.push(Win2dPrimitive::PushClip {
            clip: elwindui_core::graphics::Clip::Rect(dest),
            x: 0.0,
            y: 0.0,
        });
    }
    out.push(Win2dPrimitive::PushOpacityLayer(options.opacity.clamp(0.0, 1.0)));
    emit_vector_group(image.root(), parent_transform.concat(&local), base_opacity, out);
    out.push(Win2dPrimitive::PopOpacityLayer);
    if options.clip_to_dest {
        out.push(Win2dPrimitive::PopClip);
    }
    push_win2d_transform(out, parent_transform);
}

/// `elwindui_core::ui::RelayoutHost` for `TreeHostPanel` — wraps a *weak* reference back to the
/// panel's own tree storage (not a full owned `TreeHostPanel` clone) since a strong one would
/// create a reference cycle: this panel's own `tree` strongly holds the hosted tree's root, and
/// that root's own `UIElementImpl::invalidate_host` would then strongly hold this, right back to
/// the panel. `canvas` is captured strongly, matching `TreeHostPanel::new`'s own `SizeChanged`
/// handler below, which uses the exact same capture split (strong `canvas`, weak `tree`).
///
/// Unlike AppKit's `AppKitRelayoutHost` (where `NSView.setNeedsLayout(true)` is itself already
/// coalesced by AppKit into a single pass per display cycle, no matter how many times it's called),
/// `relayout_static` here rebuilds `Canvas.Children` synchronously and from scratch — so
/// `request_relayout` debounces via `pending` + this thread's `DispatcherQueue`, matching
/// docs/elwindui_spec.md H.2.3's `RelayoutHost` coalescing contract: repeated calls within the same
/// synchronous burst (e.g. several property setters inside one `resync()`) collapse into a single
/// deferred relayout pass, not one synchronous pass per call.
struct WinUI3RelayoutHost {
    canvas: Canvas,
    draw_canvas: CanvasControl,
    draw_list: Arc<Mutex<Vec<Win2dPrimitive>>>,
    tree: Weak<RefCell<Option<Rc<dyn elwindui_core::ui::UIElementExt>>>>,
    render_tree: Weak<RefCell<Option<elwindui_core::graphics::RenderTree>>>,
    /// `true` while a relayout pass is already enqueued on the `DispatcherQueue` and hasn't run
    /// yet — makes `request_relayout` a no-op for any further call until that pass actually runs
    /// (and clears it right before doing so).
    pending: Cell<bool>,
    /// Lets `request_relayout` (which only ever sees `&self`) hand an owned `Rc<Self>` to the
    /// `DispatcherQueueHandler` closure — set once, right after this host is `Rc`-wrapped (see
    /// `TreeHostPanel::set_tree`), the same self-referential-`Weak` pattern
    /// `InnerTabView`'s own event wiring uses for the same reason.
    weak_self: RefCell<Weak<WinUI3RelayoutHost>>,
}

impl elwindui_core::ui::RelayoutHost for WinUI3RelayoutHost {
    fn request_relayout(&self, dirty_group_id: u64) {
        if let Some(render_tree) = self.render_tree.upgrade() {
            if let Some(render_tree) = render_tree.borrow_mut().as_mut() {
                render_tree.mark_dirty(dirty_group_id);
            }
        }
        if self.pending.replace(true) {
            return; // already scheduled — the pending pass will pick up this call's changes too
        }
        let Some(this) = self.weak_self.borrow().upgrade() else {
            self.pending.set(false);
            return;
        };
        this.pending.set(false);
        if let (Some(tree), Some(render_tree)) = (this.tree.upgrade(), this.render_tree.upgrade()) {
            TreeHostPanel::relayout_static(&this.canvas, &this.draw_canvas, &this.draw_list, &tree, &render_tree);
        }
    }
}

/// `elwindui_core::ui::FocusHost` for `TreeHostPanel` — the `FocusHost` counterpart to
/// `WinUI3RelayoutHost`, same weak-back-reference shape (a strong one would create the same
/// `tree` -> `focus_host` -> panel reference cycle `WinUI3RelayoutHost`'s own doc comment
/// describes). Delegates straight to `keyboard.focus`, the single source of truth for this panel's
/// own hosted tree — mirrors `elwindui_backend_appkit::inner::AppKitFocusHost`.
struct WinUI3FocusHost {
    keyboard: Weak<KeyboardDispatcher>,
}

impl FocusHost for WinUI3FocusHost {
    fn request_focus(&self, target: &Rc<dyn elwindui_core::ui::UIElementExt>) -> bool {
        match self.keyboard.upgrade() {
            Some(keyboard) => keyboard.focus.set_focus(target, FocusState::Programmatic),
            None => false,
        }
    }
}

impl TreeHostPanel {
    pub(crate) fn new() -> Self {
        let canvas = Canvas::new().expect("Canvas::new");
        let draw_canvas = CanvasControl::new().expect("CanvasControl::new");
        let draw_list = Arc::new(Mutex::new(Vec::<Win2dPrimitive>::new()));
        let draw_list_for_handler = draw_list.clone();
        let _ = draw_canvas.Draw(&TypedEventHandler::<CanvasControl, CanvasDrawEventArgs>::new(
            move |control, args| {
                if let Some(args) = args.as_ref() {
                    let session = args.DrawingSession()?;
                    let creator = control
                        .as_ref()
                        .and_then(|control| control.cast::<ICanvasResourceCreator>().ok());
                    let mut opacity = 1.0_f32;
                    let mut active_layers = Vec::<CanvasActiveLayer>::new();
                    for primitive in draw_list_for_handler.lock().expect("Win2D draw list poisoned").iter() {
                        match primitive {
                            Win2dPrimitive::SetTransform { m11, m12, m21, m22, dx, dy } => session.SetTransform(windows_numerics::Matrix3x2 {
                                M11: *m11, M12: *m12, M21: *m21, M22: *m22, M31: *dx, M32: *dy,
                            })?,
                            Win2dPrimitive::SetOpacity(value) => opacity = *value,
                            Win2dPrimitive::SetAntialiasing(aliased) => session.SetAntialiasing(
                                if *aliased { CanvasAntialiasing::Aliased } else { CanvasAntialiasing::Antialiased },
                            )?,
                            Win2dPrimitive::SetBlend(blend) => session.SetBlend(*blend)?,
                            Win2dPrimitive::FillRect { x, y, width, height, brush } => {
                                if let Some(creator) = creator.as_ref() {
                                    let brush = win2d_brush(creator, brush, elwindui_core::base::Rect { x: *x, y: *y, width: *width, height: *height }, opacity)?;
                                    session.FillRectangleAtCoordsWithBrush(*x, *y, *width, *height, &brush)?;
                                }
                            }
                            Win2dPrimitive::StrokeRect { x, y, width, height, brush, stroke } => {
                                if let Some(creator) = creator.as_ref() {
                                    let brush = win2d_brush(creator, brush, elwindui_core::base::Rect { x: *x, y: *y, width: *width, height: *height }, opacity)?;
                                    let style = win2d_stroke_style(stroke)?;
                                    session.DrawRectangleAtCoordsWithBrushAndStrokeWidthAndStrokeStyle(
                                        *x, *y, *width, *height, &brush, stroke.width, &style,
                                    )?;
                                }
                            }
                            Win2dPrimitive::FillRoundedRect { x, y, width, height, radius, brush } => {
                                if let Some(creator) = creator.as_ref() {
                                    let brush = win2d_brush(creator, brush, elwindui_core::base::Rect { x: *x, y: *y, width: *width, height: *height }, opacity)?;
                                    session.FillRoundedRectangleAtCoordsWithBrush(*x, *y, *width, *height, *radius, *radius, &brush)?;
                                }
                            }
                            Win2dPrimitive::StrokeRoundedRect { x, y, width, height, radius, brush, stroke } => {
                                if let Some(creator) = creator.as_ref() {
                                    let brush = win2d_brush(creator, brush, elwindui_core::base::Rect { x: *x, y: *y, width: *width, height: *height }, opacity)?;
                                    let style = win2d_stroke_style(stroke)?;
                                    session.DrawRoundedRectangleAtCoordsWithBrushAndStrokeWidthAndStrokeStyle(
                                        *x, *y, *width, *height, *radius, *radius, &brush, stroke.width, &style,
                                    )?;
                                }
                            }
                            Win2dPrimitive::FillEllipse { x, y, radius_x, radius_y, brush } => {
                                if let Some(creator) = creator.as_ref() {
                                    let bounds = elwindui_core::base::Rect { x: *x - *radius_x, y: *y - *radius_y, width: *radius_x * 2.0, height: *radius_y * 2.0 };
                                    let brush = win2d_brush(creator, brush, bounds, opacity)?;
                                    session.FillEllipseAtCoordsWithBrush(*x, *y, *radius_x, *radius_y, &brush)?;
                                }
                            }
                            Win2dPrimitive::StrokeEllipse { x, y, radius_x, radius_y, brush, stroke } => {
                                if let Some(creator) = creator.as_ref() {
                                    let bounds = elwindui_core::base::Rect { x: *x - *radius_x, y: *y - *radius_y, width: *radius_x * 2.0, height: *radius_y * 2.0 };
                                    let brush = win2d_brush(creator, brush, bounds, opacity)?;
                                    let style = win2d_stroke_style(stroke)?;
                                    session.DrawEllipseAtCoordsWithBrushAndStrokeWidthAndStrokeStyle(
                                        *x, *y, *radius_x, *radius_y, &brush, stroke.width, &style,
                                    )?;
                                }
                            }
                            Win2dPrimitive::Line { x0, y0, x1, y1, brush, stroke } => {
                                if let Some(creator) = creator.as_ref() {
                                    let bounds = elwindui_core::base::Rect { x: x0.min(*x1), y: y0.min(*y1), width: (*x1 - *x0).abs(), height: (*y1 - *y0).abs() };
                                    let brush = win2d_brush(creator, brush, bounds, opacity)?;
                                    let style = win2d_stroke_style(stroke)?;
                                    session.DrawLineAtCoordsWithBrushAndStrokeWidthAndStrokeStyle(
                                        *x0, *y0, *x1, *y1, &brush, stroke.width, &style,
                                    )?;
                                }
                            }
                            Win2dPrimitive::FillPath { commands, x, y, brush, rule } => {
                                if let Some(creator) = creator.as_ref() {
                                    let geometry = win2d_path_geometry(creator, commands, *x, *y, *rule)?;
                                    let bounds = win2d_path_bounds(commands);
                                    let brush = win2d_brush(creator, brush, elwindui_core::base::Rect { x: bounds.x + *x, y: bounds.y + *y, ..bounds }, opacity)?;
                                    session.FillGeometryAtCoordsWithBrush(&geometry, 0.0, 0.0, &brush)?;
                                }
                            }
                            Win2dPrimitive::StrokePath { commands, x, y, brush, stroke } => {
                                if let Some(creator) = creator.as_ref() {
                                    let geometry = win2d_path_geometry(
                                        creator,
                                        commands,
                                        *x,
                                        *y,
                                        elwindui_core::graphics::FillRule::NonZero,
                                    )?;
                                    let bounds = win2d_path_bounds(commands);
                                    let brush = win2d_brush(creator, brush, elwindui_core::base::Rect { x: bounds.x + *x, y: bounds.y + *y, ..bounds }, opacity)?;
                                    let style = win2d_stroke_style(stroke)?;
                                    session.DrawGeometryWithBrushAndStrokeWidthAndStrokeStyle(
                                        &geometry,
                                        windows_numerics::Vector2 { X: 0.0, Y: 0.0 },
                                        &brush,
                                        stroke.width,
                                        &style,
                                    )?;
                                }
                            }
                            Win2dPrimitive::PushClip { clip, x, y } => {
                                if let Some(creator) = creator.as_ref() {
                                    let geometry = win2d_clip_geometry(creator, clip, *x, *y)?;
                                    active_layers.push(session.CreateLayerWithOpacityAndClipGeometry(1.0, &geometry)?);
                                }
                            }
                            Win2dPrimitive::PushPathStrokeClip { commands, x, y, width_px } => {
                                if let Some(creator) = creator.as_ref() {
                                    let geometry = win2d_path_geometry(
                                        creator,
                                        commands,
                                        *x,
                                        *y,
                                        elwindui_core::graphics::FillRule::NonZero,
                                    )?;
                                    let stroke_geometry = geometry.Stroke(*width_px)?;
                                    active_layers.push(session.CreateLayerWithOpacityAndClipGeometry(1.0, &stroke_geometry)?);
                                }
                            }
                            Win2dPrimitive::PopClip => {
                                active_layers.pop();
                            }
                            Win2dPrimitive::PushOpacityLayer(layer_opacity) => {
                                active_layers.push(session.CreateLayerWithOpacity(*layer_opacity)?);
                            }
                            Win2dPrimitive::PopOpacityLayer => {
                                active_layers.pop();
                            }
                            Win2dPrimitive::DrawImage { image, dest, source, options, x, y } => {
                                if let Some(creator) = creator.as_ref() {
                                    let bitmap = win2d_bitmap(creator, image)?;
                                    let size = bitmap.SizeInPixels()?;
                                    let source = source.unwrap_or(elwindui_core::base::Rect {
                                        x: 0.0, y: 0.0, width: size.Width as f32, height: size.Height as f32,
                                    });
                                    let placed = win2d_fitted_image_rect(
                                        elwindui_core::base::Rect {
                                            x: *x + dest.x, y: *y + dest.y,
                                            width: dest.width, height: dest.height,
                                        },
                                        (source.width, source.height),
                                        options,
                                    );
                                    let interpolation = match options.sampling {
                                        elwindui_core::graphics::ImageSampling::Nearest => CanvasImageInterpolation::NearestNeighbor,
                                        elwindui_core::graphics::ImageSampling::Linear => CanvasImageInterpolation::Linear,
                                        elwindui_core::graphics::ImageSampling::Cubic => CanvasImageInterpolation::HighQualityCubic,
                                    };
                                    session.DrawImageToRectWithSourceRectAndOpacityAndInterpolation(
                                        &bitmap,
                                        windows::Foundation::Rect {
                                            X: placed.x, Y: placed.y, Width: placed.width, Height: placed.height,
                                        },
                                        windows::Foundation::Rect {
                                            X: source.x, Y: source.y, Width: source.width, Height: source.height,
                                        },
                                        (opacity * options.opacity).clamp(0.0, 1.0),
                                        interpolation,
                                    )?;
                                }
                            }
                        }
                    }
                }
                Ok(())
            },
        ));
        let visual: UIElement = draw_canvas.cast().expect("CanvasControl must be a UIElement");
        canvas.Children().expect("Canvas.Children").Append(&visual).expect("append CanvasControl");
        let this = Self {
            canvas,
            draw_canvas,
            draw_list,
            tree: Rc::new(RefCell::new(None)),
            render_tree: Rc::new(RefCell::new(None)),
            keyboard: Rc::new(KeyboardDispatcher::new()),
        };
        let _ = this.draw_canvas.Invalidate();
        // WinUI3's `Control.IsTabStop` gate. Once the WinRT event projection is restored this
        // allows the host to receive OS keyboard focus, mirroring AppKit's TreeHostView.
        let _ = this.canvas.SetIsTabStop(true);
        {
            let tree_for_key = Rc::downgrade(&this.tree);
            let keyboard_for_key = Rc::downgrade(&this.keyboard);
            let callback_id = register_ui_key_event_callback(Rc::new(move |event| {
                if let (Some(tree), Some(keyboard)) = (tree_for_key.upgrade(), keyboard_for_key.upgrade()) {
                    if let Some(tree) = tree.borrow().clone() { keyboard.handle_key(&tree, event); }
                }
            }));
            let _ = this
                .canvas
                .KeyDown(&KeyEventHandler::new(
                    move |_sender, args| {
                        let Some(args) = args.cloned() else { return Ok(()); };
                        let Ok(virtual_key) = args.Key() else { return Ok(()); };
                        let Some(key) = winui_key(virtual_key) else {
                            return Ok(());
                        };
                        let is_repeat = args
                            .KeyStatus()
                            .map(|status| status.RepeatCount > 1)
                            .unwrap_or(false);
                        invoke_ui_key_event_callback(callback_id, RawKeyEvent {
                            kind: RawKeyEventKind::Down { is_repeat }, key,
                            modifiers: winui_modifiers(), timestamp_ms: 0.0,
                        });
                        Ok(())
                    },
                ));
        }
        {
            let tree_for_key = Rc::downgrade(&this.tree);
            let keyboard_for_key = Rc::downgrade(&this.keyboard);
            let callback_id = register_ui_key_event_callback(Rc::new(move |event| {
                if let (Some(tree), Some(keyboard)) = (tree_for_key.upgrade(), keyboard_for_key.upgrade()) {
                    if let Some(tree) = tree.borrow().clone() { keyboard.handle_key(&tree, event); }
                }
            }));
            let _ = this
                .canvas
                .KeyUp(&KeyEventHandler::new(
                    move |_sender, args| {
                        let Some(args) = args.cloned() else { return Ok(()); };
                        let Ok(virtual_key) = args.Key() else { return Ok(()); };
                        let Some(key) = winui_key(virtual_key) else {
                            return Ok(());
                        };
                        invoke_ui_key_event_callback(callback_id, RawKeyEvent {
                            kind: RawKeyEventKind::Up, key,
                            modifiers: winui_modifiers(), timestamp_ms: 0.0,
                        });
                        Ok(())
                    },
                ));
        }
        {
            let tree_for_text = Rc::downgrade(&this.tree);
            let keyboard_for_text = Rc::downgrade(&this.keyboard);
            let callback_id = register_ui_text_event_callback(Rc::new(move |text| {
                if let (Some(tree), Some(keyboard)) = (tree_for_text.upgrade(), keyboard_for_text.upgrade()) {
                    if let Some(tree) = tree.borrow().clone() {
                        keyboard.handle_text_input(&tree, RawTextInputEvent { text });
                    }
                }
            }));
            let _ = this.canvas.CharacterReceived(&TypedEventHandler::<
                UIElement,
            CharacterReceivedRoutedEventArgs,
            >::new(move |_sender, args| {
                let Some(args) = args.cloned() else { return Ok(()); };
                let Ok(code_unit) = args.Character() else { return Ok(()); };
                let Some(ch) = char::from_u32(code_unit as u32) else {
                    return Ok(());
                };
                if !ch.is_control() {
                    invoke_ui_text_event_callback(callback_id, ch.to_string());
                }
                Ok(())
            }));
        }
        {
        let weak = Rc::downgrade(&this.tree);
        let weak_render_tree = Rc::downgrade(&this.render_tree);
        let canvas_for_handler = this.canvas.clone();
        let draw_canvas_for_handler = this.draw_canvas.clone();
        let draw_list_for_handler = this.draw_list.clone();
        let callback_id = register_ui_event_callback(Rc::new(move || {
            if let (Some(tree), Some(render_tree)) =
                (weak.upgrade(), weak_render_tree.upgrade())
            {
                Self::relayout_static(
                    &canvas_for_handler,
                    &draw_canvas_for_handler,
                    &draw_list_for_handler,
                    &tree,
                    &render_tree,
                );
            }
        }));
        // `SizeChanged` fires whenever this panel's own allotted space changes (window resize,
        // or — for a `NativeTabView`'s per-tab content area — the tab strip/window resizing together)
        // — the same role `layout()` plays for AppKit's `TreeHostView`.
        let _ = this
            .canvas
            .SizeChanged(&SizeChangedEventHandler::new(move |_, _| {
                invoke_ui_event_callback(callback_id);
                Ok(())
            }));
        }
        this
    }

    pub(crate) fn as_element(&self) -> FrameworkElement {
        self.canvas.cast().expect("Canvas must be a FrameworkElement")
    }

    /// Forces an immediate, synchronous relayout pass against `canvas`'s *current*
    /// `ActualWidth`/`ActualHeight` — for hosts whose size is pushed in explicitly (e.g. a
    /// `TabViewItem`'s own content `Canvas`, sized by `native_ui::TabView`/`InnerTabView` rather
    /// than by native layout) rather than reliably arriving through `canvas`'s own `SizeChanged`.
    /// Confirmed necessary, not just defensive: `SetWidth`/`SetHeight` (even with
    /// `InvalidateMeasure`/`InvalidateArrange`) on such a `Canvas` does not, in practice, make its
    /// `SizeChanged` fire on any later frame either — logged and observed directly, not assumed.
    pub(crate) fn force_relayout(&self) {
        Self::relayout_static(&self.canvas, &self.draw_canvas, &self.draw_list, &self.tree, &self.render_tree);
    }

    /// Replaces this host's entire content, discarding whatever native children were there before
    /// — a full swap rather than a diff, matching `NativeTabView`'s wholesale content swap between
    /// tabs and `Window::set_content` only ever being called once (see `TreeHostView::set_tree`'s
    /// doc comment on the AppKit side for the same reasoning).
    pub(crate) fn set_tree(&self, tree: Rc<dyn elwindui_core::ui::UIElementExt>) {
        if let Ok(children) = self.canvas.Children() {
            let _ = children.Clear();
            if let Ok(visual) = self.draw_canvas.cast::<UIElement>() {
                let _ = children.Append(&visual);
            }
        }
        let host = Rc::new(WinUI3RelayoutHost {
            canvas: self.canvas.clone(),
            draw_canvas: self.draw_canvas.clone(),
            draw_list: self.draw_list.clone(),
            tree: Rc::downgrade(&self.tree),
            render_tree: Rc::downgrade(&self.render_tree),
            pending: Cell::new(false),
            weak_self: RefCell::new(Weak::new()),
        });
        *host.weak_self.borrow_mut() = Rc::downgrade(&host);
        tree.as_ui_element().set_invalidate_host(Some(host));
        tree.as_ui_element()
            .set_focus_host(Some(Rc::new(WinUI3FocusHost {
                keyboard: Rc::downgrade(&self.keyboard),
            })));
        self.keyboard.focus.clear_focus();
        self.keyboard.shortcuts().clear();
        collect_shortcuts_into(&tree, self.keyboard.shortcuts());
        *self.tree.borrow_mut() = Some(tree);
        *self.render_tree.borrow_mut() = None;
        Self::relayout_static(&self.canvas, &self.draw_canvas, &self.draw_list, &self.tree, &self.render_tree);
    }

    fn relayout_static(
        canvas: &Canvas,
        draw_canvas: &CanvasControl,
        draw_list: &Arc<Mutex<Vec<Win2dPrimitive>>>,
        tree: &Rc<RefCell<Option<Rc<dyn elwindui_core::ui::UIElementExt>>>>,
        retained_tree: &Rc<RefCell<Option<elwindui_core::graphics::RenderTree>>>,
    ) {
        use elwindui_core::base::Size as LSize;

        // `ActualWidth`/`ActualHeight` only update after a real native layout pass runs on this
        // element, which never happens for a panel used as `TabViewItem.Content` (see
        // `force_relayout`'s doc comment). When a caller has explicitly set `Width`/`Height` (not
        // `NaN`, the "unset" sentinel) ahead of calling this — e.g. `InnerTabView`'s resize
        // callback — that value is authoritative and already reflects the real available size, so
        // prefer it over the possibly-stale `ActualWidth`/`ActualHeight`.
        let explicit_width = canvas.Width().unwrap_or(f64::NAN);
        let explicit_height = canvas.Height().unwrap_or(f64::NAN);
        let width = if explicit_width.is_finite() {
            explicit_width as f32
        } else {
            canvas.ActualWidth().unwrap_or(0.0) as f32
        };
        let height = if explicit_height.is_finite() {
            explicit_height as f32
        } else {
            canvas.ActualHeight().unwrap_or(0.0) as f32
        };
        let available = LSize { width, height };
        if let Ok(surface) = draw_canvas.cast::<FrameworkElement>() {
            let _ = surface.SetWidth(width as f64);
            let _ = surface.SetHeight(height as f64);
        }

        let tree_ref = tree.borrow();
        let Some(tree) = tree_ref.as_ref() else {
            return;
        };
        elwindui_core::ui::layout_root(tree, available);
        {
            let mut retained_tree = retained_tree.borrow_mut();
            if retained_tree
                .as_ref()
                .is_some_and(|render_tree| render_tree.root_id() == tree.render_group_id())
            {
                retained_tree
                    .as_mut()
                    .expect("checked above")
                    .reconcile::<AnyView>(tree);
            } else {
                *retained_tree = Some(elwindui_core::graphics::RenderTree::new::<AnyView>(tree));
            }
        }
        let retained_tree = retained_tree.borrow();
        let Some(render_tree) = retained_tree.as_ref() else {
            return;
        };

        let Ok(children) = canvas.Children() else {
            return;
        };
        let _ = children.Clear();
        if let Ok(visual) = draw_canvas.cast::<UIElement>() {
            let _ = children.Append(&visual);
        }
        let mut primitives = Vec::new();
        let mut transforms = vec![elwindui_core::base::AffineTransform::identity()];
        let mut opacities = vec![1.0_f32];
        primitives.push(Win2dPrimitive::SetTransform {
            m11: 1.0, m12: 0.0, m21: 0.0, m22: 1.0, dx: 0.0, dy: 0.0,
        });
        primitives.push(Win2dPrimitive::SetOpacity(1.0));

        fn collect_commands<'a>(
            group: &'a elwindui_core::graphics::RenderGroup,
            origin: elwindui_core::base::Point,
            out: &mut Vec<(
                &'a elwindui_core::graphics::RenderCommand,
                elwindui_core::base::Point,
            )>,
        ) {
            let origin = elwindui_core::base::Point {
                x: origin.x + group.offset.x,
                y: origin.y + group.offset.y,
            };
            for command in &group.commands {
                out.push((command, origin));
            }
            for child in &group.children {
                collect_commands(child, origin, out);
            }
        }
        let mut commands = Vec::new();
        collect_commands(
            &render_tree.root,
            elwindui_core::base::Point { x: 0.0, y: 0.0 },
            &mut commands,
        );
        // Win2D commands are retained as plain values and replayed from CanvasControl::Draw.
        // This keeps primitive figures, images, SVG paths, and clip/opacity layers on the same
        // device/context, while XAML controls and text remain normal children of the host Canvas.
        for (command, origin) in commands {
            match command {
                elwindui_core::graphics::RenderCommand::PushTransform { transform } => {
                    let next = transforms.last().expect("transform stack").concat(transform);
                    transforms.push(next);
                    primitives.push(Win2dPrimitive::SetTransform {
                        m11: next.m11, m12: next.m12, m21: next.m21, m22: next.m22,
                        dx: next.dx, dy: next.dy,
                    });
                    continue;
                }
                elwindui_core::graphics::RenderCommand::PopTransform => {
                    if transforms.len() > 1 { transforms.pop(); }
                    let current = *transforms.last().expect("transform stack");
                    primitives.push(Win2dPrimitive::SetTransform {
                        m11: current.m11, m12: current.m12, m21: current.m21, m22: current.m22,
                        dx: current.dx, dy: current.dy,
                    });
                    continue;
                }
                elwindui_core::graphics::RenderCommand::PushOpacity { opacity } => {
                    let next = opacities.last().expect("opacity stack") * opacity;
                    opacities.push(next);
                    primitives.push(Win2dPrimitive::SetOpacity(next));
                    continue;
                }
                elwindui_core::graphics::RenderCommand::PushClip { clip } => {
                    primitives.push(Win2dPrimitive::PushClip { clip: clip.clone(), x: origin.x, y: origin.y });
                    continue;
                }
                elwindui_core::graphics::RenderCommand::PopClip => {
                    primitives.push(Win2dPrimitive::PopClip);
                    continue;
                }
                elwindui_core::graphics::RenderCommand::PopOpacity => {
                    if opacities.len() > 1 { opacities.pop(); }
                    primitives.push(Win2dPrimitive::SetOpacity(*opacities.last().expect("opacity stack")));
                    continue;
                }
                _ => {}
            }
            match command {
                elwindui_core::graphics::RenderCommand::FillRect { rect, brush } => primitives.push(Win2dPrimitive::FillRect {
                    x: origin.x + rect.x, y: origin.y + rect.y, width: rect.width, height: rect.height,
                    brush: brush.clone(),
                }),
                elwindui_core::graphics::RenderCommand::StrokeRect { rect, brush, stroke } => primitives.push(Win2dPrimitive::StrokeRect {
                    x: origin.x + rect.x, y: origin.y + rect.y, width: rect.width, height: rect.height,
                    brush: brush.clone(), stroke: stroke.clone(),
                }),
                elwindui_core::graphics::RenderCommand::FillRoundedRect { rect, radii, brush } => primitives.push(Win2dPrimitive::FillRoundedRect {
                    x: origin.x + rect.x, y: origin.y + rect.y, width: rect.width, height: rect.height,
                    radius: (radii.top_left + radii.top_right + radii.bottom_right + radii.bottom_left) / 4.0,
                    brush: brush.clone(),
                }),
                elwindui_core::graphics::RenderCommand::StrokeRoundedRect { rect, radii, brush, stroke } => primitives.push(Win2dPrimitive::StrokeRoundedRect {
                    x: origin.x + rect.x, y: origin.y + rect.y, width: rect.width, height: rect.height,
                    radius: (radii.top_left + radii.top_right + radii.bottom_right + radii.bottom_left) / 4.0,
                    brush: brush.clone(), stroke: stroke.clone(),
                }),
                elwindui_core::graphics::RenderCommand::FillEllipse { rect, brush } => primitives.push(Win2dPrimitive::FillEllipse {
                    x: origin.x + rect.x + rect.width / 2.0, y: origin.y + rect.y + rect.height / 2.0,
                    radius_x: rect.width / 2.0, radius_y: rect.height / 2.0,
                    brush: brush.clone(),
                }),
                elwindui_core::graphics::RenderCommand::StrokeEllipse { rect, brush, stroke } => primitives.push(Win2dPrimitive::StrokeEllipse {
                    x: origin.x + rect.x + rect.width / 2.0, y: origin.y + rect.y + rect.height / 2.0,
                    radius_x: rect.width / 2.0, radius_y: rect.height / 2.0,
                    brush: brush.clone(), stroke: stroke.clone(),
                }),
                elwindui_core::graphics::RenderCommand::DrawLine {
                    from,
                    to,
                    brush,
                    stroke,
                } => {
                    primitives.push(Win2dPrimitive::Line {
                        x0: origin.x + from.x, y0: origin.y + from.y,
                        x1: origin.x + to.x, y1: origin.y + to.y,
                        brush: brush.clone(), stroke: stroke.clone(),
                    });
                }
                elwindui_core::graphics::RenderCommand::Text {
                    content,
                    rect,
                    color,
                    alignment,
                    ..
                } => {
                    let text_block = TextBlock::new().expect("TextBlock::new");
                    let _ = text_block.SetText(&HSTRING::from(content));
                    if let Ok(brush) = solid_color_brush(color.unwrap_or(elwindui_core::graphics::Color::black())) {
                        let _ = text_block.SetForeground(&brush);
                    }
                    let _ = text_block.SetTextAlignment(xaml_text_alignment(*alignment));
                    let fe: FrameworkElement = text_block.cast().expect("TextBlock must be a FrameworkElement");
                    let _ = fe.SetWidth(rect.width as f64);
                    let _ = fe.SetHeight(rect.height as f64);
                    let _ = Canvas::SetLeft(&fe, (origin.x + rect.x) as f64);
                    let _ = Canvas::SetTop(&fe, (origin.y + rect.y) as f64);
                    let _ = children.Append(&fe);
                }
                elwindui_core::graphics::RenderCommand::NativeControl { handle, rect, .. } => {
                    if let Some(mut view) = handle.downcast_ref::<AnyView>().cloned() {
                        view.arrange(elwindui_core::base::Rect {
                            x: origin.x + rect.x,
                            y: origin.y + rect.y,
                            width: rect.width,
                            height: rect.height,
                        });
                        let _ = children.Append(&view.as_element());
                    }
                }
                elwindui_core::graphics::RenderCommand::FillPath { path, brush, rule } => primitives.push(Win2dPrimitive::FillPath {
                    commands: path.commands().to_vec(), x: origin.x, y: origin.y,
                    brush: brush.clone(), rule: *rule,
                }),
                elwindui_core::graphics::RenderCommand::StrokePath { path, brush, stroke } => primitives.push(Win2dPrimitive::StrokePath {
                    commands: path.commands().to_vec(), x: origin.x, y: origin.y,
                    brush: brush.clone(), stroke: stroke.clone(),
                }),
                elwindui_core::graphics::RenderCommand::DrawImage { image, dest, source, options } => primitives.push(Win2dPrimitive::DrawImage {
                    image: image.clone(), dest: *dest, source: *source, options: *options,
                    x: origin.x, y: origin.y,
                }),
                elwindui_core::graphics::RenderCommand::DrawVectorImage { image, dest, source, options } => {
                    let dest = elwindui_core::base::Rect {
                        x: origin.x + dest.x,
                        y: origin.y + dest.y,
                        width: dest.width,
                        height: dest.height,
                    };
                    emit_vector_image(
                        image,
                        dest,
                        *source,
                        options,
                        *transforms.last().expect("transform stack"),
                        *opacities.last().expect("opacity stack"),
                        &mut primitives,
                    );
                }
                elwindui_core::graphics::RenderCommand::PushClip { .. }
                | elwindui_core::graphics::RenderCommand::PopClip
                | elwindui_core::graphics::RenderCommand::PushTransform { .. }
                | elwindui_core::graphics::RenderCommand::PopTransform
                | elwindui_core::graphics::RenderCommand::PushOpacity { .. }
                | elwindui_core::graphics::RenderCommand::PopOpacity => {
                    unreachable!("state commands are handled by the preceding replay branch")
                }
            }
        }
        *draw_list.lock().expect("Win2D draw list poisoned") = primitives;
        let _ = draw_canvas.Invalidate();
    }
}

/// Converts our own `elwindui_core::graphics::Color` (RGBA field order) into a `Windows::UI::Color`
/// (ARGB field order) — a plain field re-shuffle, no hex round-trip needed now that `Color` is a
/// real value type rather than a backend-agnostic hex string (painter design doc §18).
fn graphics_color_to_winui_color(c: elwindui_core::graphics::Color) -> Color {
    Color {
        A: c.a,
        R: c.r,
        G: c.g,
        B: c.b,
    }
}

fn solid_color_brush(color: elwindui_core::graphics::Color) -> Result<SolidColorBrush> {
    let brush = SolidColorBrush::new()?;
    brush.SetColor(graphics_color_to_winui_color(color))?;
    Ok(brush)
}

/// `elwindui_core::ui::TextAlignment` -> `Microsoft.UI.Xaml.TextAlignment`.
fn xaml_text_alignment(
    alignment: elwindui_core::ui::TextAlignment,
) -> bindings::Microsoft::UI::Xaml::TextAlignment {
    match alignment {
        elwindui_core::ui::TextAlignment::Left => {
            bindings::Microsoft::UI::Xaml::TextAlignment::Left
        }
        elwindui_core::ui::TextAlignment::Center => {
            bindings::Microsoft::UI::Xaml::TextAlignment::Center
        }
        elwindui_core::ui::TextAlignment::Right => {
            bindings::Microsoft::UI::Xaml::TextAlignment::Right
        }
    }
}

